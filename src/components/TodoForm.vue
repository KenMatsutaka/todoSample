<template>
  <div>
    <h3>My Todo</h3>
    <input type="text" v-model="newTodo" placeholder="Input hereâ€¦" />
    <button @click="addTodo()">ADD</button>
  </div>
</template>

<script>
export default {
  data() {
    return {
      newTodo: ""
    };
  },
  methods: {
    addTodo() {
      if (this.newTodo === "") {
        return;
      }
      this.$emit("add", this.newTodo);
      this.newTodo = "";
    }
  }
};
</script>
